/**
  * op_add - adds two ints
  * @a: int 1 to be added
  * @b: int 2 to be added
  * Return: the sum of the two as an int
  */
int op_add(int a, int b)
{
	return (a + b);
}
/**
  * op_sub - subtracts two ints
  * @a: int 1
  * @b: what is subtracted from a
  * Return: the difference between the two as an int
  */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
  * op_mul - determines the product of two ints
  * @a: int 1
  * @b: int 2
  * Return: the product as an int
  */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
  * op_div - determines the result of division of two numbers
  * @a: number 1
  * @b: the number that is used to divide a
  * Return: the result as an int
  */
int op_div(int a, int b)
{
	return (a / b);
}
/**
  * op_mod - determines the modulus
  * @a: int 1
  * @b: int 2
  * Return: the modulus of the two ints
  */
int op_mod(int a, int b)
{
	return (a % b);
}
